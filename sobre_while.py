#o while funciona como um bloco de código que vai se repetir SE uma condição for verdadeira

i = 1                   #variável contadora (o loop vai começar a partir desse número)
while i < 6 or i == 6:      #condição ao lado do while sempre
    print(i)
    i += 1              #variável incrementadora (o loop vai rodar a partir da contadora + incremento)
